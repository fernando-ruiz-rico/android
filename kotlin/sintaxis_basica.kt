fun main() {
    // Sin saltos de línea
    print("Hello, world!")
    print("Hello, world!")

    // Con saltos de línea
    print("Hello, world!\n")
    print("Hello, world!\n")

    println("Hello, world!")
    println("Hello, world!")

    // Constantes
    val popcorn = 5
    println(popcorn)
    val hotdog = 7
    println(hotdog)

    // Variables
    var customers = 10
    println(customers)
    customers = 8
    println(customers)

    // Plantillas de cadenas
    println("There are $customers customers")
    println("There are ${customers + 1} customers")

    // Complete the code to make the program print "Mary is 20 years old" to standard output
    val name = "Mary"
    val age = 20
    println("$name is $age years old")
}